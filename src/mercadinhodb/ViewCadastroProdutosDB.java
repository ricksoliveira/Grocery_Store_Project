    public void readTable(){
        DefaultTableModel modelo = (DefaultTableModel) jProductTable.getModel();
        
        modelo.setNumRows(0);
        
        ProdutoDAO pdao = new ProdutoDAO();
        
        for(Produto p : pdao.read()){
            modelo.addRow(new Object[]{p.getId(), p.getDescricao(), p.getQuantidade(), p.getPreco(), p.getCriacao(), p.getAlteracao()});
        }
        
    }
    
    public void readSearch(String desc){
        DefaultTableModel modelo = (DefaultTableModel) jProductTable.getModel();
        
        modelo.setNumRows(0);
        
        ProdutoDAO pdao = new ProdutoDAO();
        
        for(Produto p : pdao.search(desc)){
            modelo.addRow(new Object[]{p.getId(), p.getDescricao(), p.getQuantidade(), p.getPreco(), p.getCriacao(), p.getAlteracao()});
        }
        
    }
    
