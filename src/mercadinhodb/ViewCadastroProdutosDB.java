    private void jBotAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotAtualizarActionPerformed

        if(jProductTable.getSelectedRow() != -1){

            if("".equals(txtDesc.getText()) || "".equals(txtQuantidade.getText()) || "".equals(txtPreco.getText())){
                JOptionPane.showMessageDialog(null, "Preencha os campos para atualizar este produto");
            }
            else{
                Produto p = new Produto();
                ProdutoDAO dao = new ProdutoDAO();

                p.setAlteracao(ViewLauncherDB.Login_User);
                p.setId((int) jProductTable.getValueAt(jProductTable.getSelectedRow(), 0));
                p.setDescricao(txtDesc.getText());
                p.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
                p.setPreco(Double.parseDouble(txtPreco.getText()));

                dao.update(p);
                txtDesc.setText("");
                txtQuantidade.setText("");
                txtPreco.setText("");
                readTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um produto para atualizar!");
        }
    }//GEN-LAST:event_jBotAtualizarActionPerformed

