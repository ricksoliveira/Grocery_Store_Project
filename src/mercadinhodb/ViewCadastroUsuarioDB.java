    private void botao_Remover_UsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_Remover_UsuarioActionPerformed
        
        if(UserTable.getSelectedRow() != -1){

            Usuario u = new Usuario();
            UsuarioDAO udao = new UsuarioDAO();

            u.setLogin_usuario((String) UserTable.getValueAt(UserTable.getSelectedRow(), 0));

            udao.deleteUser(u);
            txtUsuario.setText("");
            txtSenha.setText("");
            readTable();

        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir!");
        }
        
    }//GEN-LAST:event_botao_Remover_UsuarioActionPerformed

    private void botao_CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_CadastrarActionPerformed
        
        if("".equals(txtUsuario.getText()) || "".equals(txtSenha.getText())){
            JOptionPane.showMessageDialog(null, "Preencha todas os campos.");
        }
        else{
            Usuario u = new Usuario();
            UsuarioDAO udao = new UsuarioDAO();
            
            if("1 - Usuário".equals(ComboBox.getSelectedItem().toString())){
                u.setAdmin(1);
            }
            else if("2 - Caixa".equals(ComboBox.getSelectedItem().toString())){
                u.setAdmin(2);
            }
            
            u.setLogin_usuario(txtUsuario.getText());
            u.setSenha(txtSenha.getText());
            
            udao.createUser(u);
            txtUsuario.setText("");
            txtSenha.setText("");
            ComboBox.setSelectedIndex(-1);
            readTable();
        }
        
    }//GEN-LAST:event_botao_CadastrarActionPerformed

