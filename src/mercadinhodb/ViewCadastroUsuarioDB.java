    public void readTable(){
        DefaultTableModel modelo = (DefaultTableModel) UserTable.getModel();
        
        modelo.setNumRows(0);
        
        UsuarioDAO udao = new UsuarioDAO();
        
        for(Usuario u : udao.readUser()){
            switch(u.getAdmin()){
                case 0:
                    modelo.addRow(new Object[]{u.getLogin_usuario(), u.getSenha(), "Administrador"});
                    break;
                    
                case 1:
                    modelo.addRow(new Object[]{u.getLogin_usuario(), u.getSenha(), "Usuário"});
                    break;
                    
                case 2:
                    modelo.addRow(new Object[]{u.getLogin_usuario(), u.getSenha(), "Caixa"});
                    break;
                    
                default:
                    modelo.addRow(new Object[]{u.getLogin_usuario(), u.getSenha(), "Sem função"});
                    break;
            }
        }
        
    }
    
    public void readSearch(String desc){
        DefaultTableModel modelo = (DefaultTableModel) UserTable.getModel();
        
        modelo.setNumRows(0);
        
        UsuarioDAO udao = new UsuarioDAO();
        
        for(Usuario u : udao.search(desc)){
            if(u.getAdmin() == 0){
                modelo.addRow(new Object[]{u.getLogin_usuario(), u.getSenha(), "Usuário"});
            }
            else{
                modelo.addRow(new Object[]{u.getLogin_usuario(), u.getSenha(), "Administrador"});
            }
        }
        
    }
    
